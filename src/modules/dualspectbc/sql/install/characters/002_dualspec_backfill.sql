-- DualSpec module: backfill character_talent from pre-existing character_spell
-- rows.
--
-- Why this file exists: TBC's stock characters DB has no `character_talent`
-- table — talent investments live implicitly in `character_spell`. Once the
-- module is enabled, talent reads/writes use `character_talent`; without
-- backfill, every existing character logs in with zero talents recorded and
-- M3 (spec swap) silently no-ops.
--
-- The talent spell-id set was generated from wow_dbc.db_Talent_8606
-- (build 8606 = TBC 2.4.3, ranks 1-5; ranks 6-9 are unused for this build),
-- 1732 distinct ids, sorted ascending. Inlined here because wow-dbc and
-- cmangos-tbc's characters DB are separate MariaDB / MySQL instances per
-- CLAUDE.md, so a live cross-instance JOIN is not possible.
